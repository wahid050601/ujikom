    <script>
        $('#login-btn').on('click', function(){
            let users = $('#users').val();
            let pass = $('#pass').val();

            if(users == ''){
                Swal.fire({
                    title: "warning",
                    text: "Username harus diisi",
                    icon: "warning",
                });
            }else if(pass == ''){
                Swal.fire({
                    title: "warning",
                    text: "Password harus diisi",
                    icon: "warning",
                });
            }else{
                $.ajax({
                    url: 'login-act.php',
                    method: 'post',
                    dataType: 'json',
                    data: {
                        'action': 'login',
                        'username': users,
                        'password': pass
                    },
                    success: function(logins){
                        if(logins.status == 'success'){
                            localStorage.setItem('loginToken',users + pass);
                            console.log(logins);
                            localStorage.setItem('user', logins.user.nama_adm);
                            localStorage.setItem('id', logins.user.id_adm);
                            localStorage.setItem('role', logins.user.role_adm);

                            window.location.href = "/apps1/";
                        }else if(logins.status == 'error'){

                            Swal.fire({
                                title: logins.status,
                                text: logins.info,
                                icon: logins.status,
                            });
                        }
                    }
                });
            }
        });
    </script>

